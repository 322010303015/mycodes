class question20
{
    public static double square(double n)
    {
        return n*n;
    }
    public static double cube(double n)
    {
        return n*n*n;
    }
    public static void main(String[] args)
    {
        System.out.println("The square of the is"+square(3));
        System.out.println("The cube of the is"+cube(3));
    }

}